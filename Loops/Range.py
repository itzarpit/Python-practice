#prints number from 0 to 7
for i in range(8):
    print(i)

#wants number to be printed from a specific digit we use range(start digit, end digit)
for i in range(2,8): #prints number from 2 to 7
    print(i)

#wants number to be printed with a specific gap in between them we can use range(start, end , step_count)
for i in range(1,11,2): # prints number with a gap of two
    print(i)